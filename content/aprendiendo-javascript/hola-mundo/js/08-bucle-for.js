'use strict'

// Bucle FOR
// Un bucle es una estructura de control que se repite varias veces

// Mostrar los números del 1 al 100
var numero = 100;

for(var i = 0; i <= numero; i++) { // contador + condición + incrementador
    console.log('Vamos por el número ' + i);

    debugger;  // para ir viendo visualmente en consola como funciona el bucle
}